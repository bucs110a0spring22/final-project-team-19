class Rectangle:
  def __init__(self, x, y, height, width):
    self.x = x
    self.y = y
    self.height = height
    self.width = width


      
  def __str__(self):
    r1 = Rectangle(x, y, height, width)
    return "x:", r1.x
    return "y: ", r1.y
    return "height: ", r1.height
    return "width: ", r1.width